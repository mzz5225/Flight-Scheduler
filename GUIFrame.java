    private void removeWaitlistButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeWaitlistButtonActionPerformed
        String displayMessage = "";
        String customer = rWCustomerComboBox.getSelectedItem().toString();
        String flight2 = rWFlightComboBox.getSelectedItem().toString();
        String date = rWDateComboBox.getSelectedItem().toString();
        
        boolean successful = waitlist.removeWaitlistEntry(customer, flight2, date);
        
        ArrayList<String> days3 = day.getCustomerWaitlistDays((String) rWCustomerComboBox.getSelectedItem());
        rWDateComboBox.removeAllItems();
        for (String rWDate : days3) {
            rWDateComboBox.addItem(rWDate);
        }
        
        ArrayList<String> flights3 = flight.getCustomerWaitlistFlights(
                (String) rWCustomerComboBox.getSelectedItem(), 
                (String) rWDateComboBox.getSelectedItem()
        );
        rWFlightComboBox.removeAllItems();
        for (String rWFlight : flights3) {
            rWFlightComboBox.addItem(rWFlight);
        }
        
        if (successful == true) {
            displayMessage = "Waitlist entry succesfully removed!";
        } else {
            displayMessage = "Something went wrong!";
        }
        
        flightWaitlistTextArea.setText(displayMessage);
    }//GEN-LAST:event_removeWaitlistButtonActionPerformed

    private void removeFlightButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeFlightButtonActionPerformed
        // TODO add your handling code here:
        String result = flight.removeFlight((String) rFFlightComboBox.getSelectedItem());
        flightBookingTextArea.setText(result);
        
        ArrayList<String> customers = customer.getCustomers();
        rBCustomerComboBox.removeAllItems();
        rWCustomerComboBox.removeAllItems();
        for (String customerName : customers) {
            rBCustomerComboBox.addItem(customerName);
            rWCustomerComboBox.addItem(customerName);
        }
        
        ArrayList<String> flights = flight.getFlights();
        flightComboBox.removeAllItems();
        flightComboBox2.removeAllItems();
        rFFlightComboBox.removeAllItems();
        for (String flightName : flights) {
            flightComboBox.addItem(flightName);
            flightComboBox2.addItem(flightName);
            rFFlightComboBox.addItem(flightName);
        }
    }//GEN-LAST:event_removeFlightButtonActionPerformed

