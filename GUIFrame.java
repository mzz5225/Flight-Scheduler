    private void addBookingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookingButtonActionPerformed
        String customer2 = customerComboBox.getSelectedItem().toString();
        String flight5 = flightComboBox.getSelectedItem().toString();
        String date = dateComboBox.getSelectedItem().toString();
        boolean isFilled = bookingEntry.checkIfBooked(customer2, flight5, date);
        boolean alreadyBooked;
        boolean alreadyOnWaitlist;
        boolean onList;

        if (isFilled == false) {
            alreadyBooked = bookingEntry.setBookingEntry(customer2, flight5, date);

            if (alreadyBooked == false) {
                ArrayList<String> customers = customer.getCustomers();
                rBCustomerComboBox.removeAllItems();
                rWCustomerComboBox.removeAllItems();
                for (String customerName : customers) {
                    rBCustomerComboBox.addItem(customerName);
                    rWCustomerComboBox.addItem(customerName);
                }
                
                ArrayList<String> days2 = day.getCustomerBookingDays((String) rBCustomerComboBox.getSelectedItem());
                rBDateComboBox.removeAllItems();
                for (String rBDate : days2) {
                    rBDateComboBox.addItem(rBDate);
                }
        
                ArrayList<String> flights2 = flight.getCustomerBookingFlights(
                        (String) rBCustomerComboBox.getSelectedItem(), 
                        (String) rBDateComboBox.getSelectedItem()
                );
                rBFlightComboBox.removeAllItems();
                for (String rBFlight : flights2) {
                    rBFlightComboBox.addItem(rBFlight);
                }
                
                ArrayList<String> days3 = day.getCustomerWaitlistDays((String) rWCustomerComboBox.getSelectedItem());
                rWDateComboBox.removeAllItems();
                for (String rWDate : days3) {
                    rWDateComboBox.addItem(rWDate);
                }
        
                ArrayList<String> flights3 = flight.getCustomerWaitlistFlights(
                        (String) rWCustomerComboBox.getSelectedItem(), 
                        (String) rWDateComboBox.getSelectedItem()
                );
                rWFlightComboBox.removeAllItems();
                for (String rWFlight : flights3) {
                    rWFlightComboBox.addItem(rWFlight);
                }
                
                bookingStatusTextArea.setText("Sucessfully booked!");
            } else {
                bookingStatusTextArea.setText("Already booked this flight!");
            }
        } else {
            onList = bookingEntry.onBookingList(customer2);

            if (onList == false) {
                alreadyOnWaitlist = waitlist.addWaitlistEntry(customer2, flight5, date);

                if (alreadyOnWaitlist == false) {
                    bookingStatusTextArea.setText("No space left! Added to waitlist.");
                } else {
                    bookingStatusTextArea.setText("Already on waitlist!");
                }
            } else {
                bookingStatusTextArea.setText("Already booked this flight!");
            }
        }
    }//GEN-LAST:event_addBookingButtonActionPerformed

